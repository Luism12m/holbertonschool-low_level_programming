#include "main.h"
#define PI 3.14159

// Implementacinnn de la funcion para calcular el area de un circulo

float calcularAreaCirculo(float radio) {
  return PI * radio * radio;
}

// Implementacinnn de la funcion para calcular el area d eun rectangulo
float calcularAreaRectangulo(float base, float altura) {
  return base * altura;
}
